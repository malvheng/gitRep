package com.itgod.entity;

public class User {
	private String id;
	private String useNme;
	private String usePaw;
	private String usePho;
	private String useEma;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUseNme() {
		return useNme;
	}

	public void setUseNme(String useNme) {
		this.useNme = useNme;
	}

	public String getUsePaw() {
		return usePaw;
	}

	public void setUsePaw(String usePaw) {
		this.usePaw = usePaw;
	}

	public String getUsePho() {
		return usePho;
	}

	public void setUsePho(String usePho) {
		this.usePho = usePho;
	}

	public String getUseEma() {
		return useEma;
	}

	public void setUseEma(String useEma) {
		this.useEma = useEma;
	}

}
